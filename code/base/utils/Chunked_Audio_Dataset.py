from pathlib import Path
import torch
from torch.utils.data import Dataset, DataLoader

import soundfile as sf # load wav

from configs.CFG_PATH import PATH
from configs.CFG_PATH import CFG

"""
Load 4-second chunked audio files for classification.

This dataloader reads chunked wav files generated by the chunking script.
Each filename contains its class name in the format:

    <wav_id>-<chunk_id>-<class>.wav

Example:
    0000-00-alarm.wav -> class name: alarm -> label: 0

The audio waveform is loaded as float32 and returned together with
the integer class label defined in CFG.dict_class_labels.
"""
class ChunkedAudioDataset(Dataset):
    """
    input: folder path;
    output: x, label, audio file path
    """
    def __init__(self,chunk_dir):
        self.chunk_dir = Path(chunk_dir)
        self.audio_files_pathlist = sorted(self.chunk_dir.glob('*.wav'))

        self.sample_rate = CFG.sample_rate
        self.clip_samples = CFG.clip_samples
        self.dict_class_labels = CFG.dict_class_labels

    def __len__(self):
        return len(self.audio_files_pathlist)

    def __getitem__(self, idx):
        audio_files_path = self.audio_files_pathlist[idx]

        # example:0000-00-classA.wav;
        # OUTPUT:classA
        label_str_name = audio_files_path.stem.split('-')[-1]

        if label_str_name not in self.dict_class_labels:
            raise ValueError(f"{label_str_name} not in {self.dict_class_labels}")
        label = self.dict_class_labels[label_str_name]

        # ==== get x &and& basic check
        x,file_sr = sf.read(audio_files_path)
        if file_sr != self.sample_rate:
            raise ValueError(f"Sample rate mismatch: {audio_files_path}, "
                f"file_sr={file_sr}, expected={self.sample_rate}")
        if len(x) != self.clip_samples:
            raise ValueError(f"Expected {self.clip_samples} samples, but got {len(x)}")
        if x.ndim == 2:
            x = x.mean(axis=1)

        x = torch.from_numpy(x).float()
        label = torch.tensor(label).long()
        return x,label,str(audio_files_path)


